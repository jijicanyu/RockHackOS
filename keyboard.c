#include "keyboard.h"

uint8_t key_translate[] = {
 // 0x00  0x01  0x02  0x03  0x04  0x05  0x06  0x07
 // 0x08  0x09  0x0A  0x0B  0x0C  0x0D  0x0E  0x0F
    0x00, 0x00, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, // 0x00
    0x37, 0x38, 0x39, 0x30, 0x00, 0x00, 0xFF, 0x00, // 0x08
    0x71, 0x77, 0x65, 0x72, 0x74, 0x79, 0x75, 0x69, // 0x10
    0x6F, 0x70, 0x00, 0x00, 0x0A, 0x00, 0x61, 0x73, // 0x18
    0x64, 0x66, 0x67, 0x68, 0x6A, 0x6B, 0x6C, 0x3B, // 0x20
    0x00, 0x00, 0x00, 0x00, 0x7A, 0x78, 0x63, 0x76, // 0x28
    0x62, 0x6E, 0x6D, 0x2C, 0x2E, 0x2F, 0x00, 0x00, // 0x30
    0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 0x38
};

uint8_t do_key_translate(uint8_t keycode) {
    if (keycode < 0x40) {
        return key_translate[keycode];
    } else {
        return 0x00;
    }
}

uint8_t last_keycode = 0;

void _keyboard_int(int keycode) {
    uint8_t keycode8 = keycode & 0xFF;
    uint8_t realchar = do_key_translate(keycode8);
    if (realchar != 0x00) {
        last_keycode = keycode8;
        hexes_write_char(do_key_translate(keycode8), 0, 2);
    }
}

uint8_t get_last_key() {
    return last_keycode;
}
